    protected void autonumber() {
        try {
            String sql = "SELECT kode_inventaris_keluar FROM transaksi_inventaris_keluar ORDER BY kode_inventaris_keluar DESC LIMIT 1";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
    
            int nextNumber = 1;
    
            if (hasil.next()) {
                String lastId = hasil.getString("kode_inventaris_keluar");
                String lastNumberStr = lastId.substring(5);
                
                try {
                    int lastNumber = Integer.parseInt(lastNumberStr);
                    nextNumber = lastNumber + 1;
                } catch (NumberFormatException ex) {
                    // Handle non-numeric characters in the ID
                    JOptionPane.showMessageDialog(null, "Invalid numeric format in the ID");
                    return;
                }
            }
    
            String formattedNumber = String.format("%04d", nextNumber);
            txtidinv.setText("KELUAR" + formattedNumber);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto number gagal " + e);
        }
    }
    
    
    
