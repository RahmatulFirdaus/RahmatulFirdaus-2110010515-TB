    protected void tabelData(){
        Object[] Baris = {"ID Inventaris Keluar", "ID Inventaris Masuk", "Tanggal Keluar","Jumlah","Keterangan"};
        tabmod = new DefaultTableModel(null, Baris);
        String cariBarang = tfCari.getText();            
        try{
            String sql = "SELECT * FROM transaksi_inventaris_keluar WHERE kode_inventaris_keluar like '%"+cariBarang+"%' order by kode_inventaris_keluar asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                tabmod.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                });
            }
            tabell.setModel(tabmod);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal di panggil"+e);
        }
    }
