    protected void autonumber() {
        try {
            String sql = "SELECT kode_inventaris_masuk FROM transaksi_inventaris_masuk ORDER BY kode_inventaris_masuk DESC LIMIT 1";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
    
            int nextNumber = 1;
    
            if (hasil.next()) {
                String lastId = hasil.getString("kode_inventaris_masuk");
                int lastNumber = Integer.parseInt(lastId.substring(5));
    
                nextNumber = lastNumber + 1;
            }
    
            String formattedNumber = String.format("%04d", nextNumber);
            txtidinv.setText("MASUK" + formattedNumber);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto number gagal " + e);
        }
    }
    
    
