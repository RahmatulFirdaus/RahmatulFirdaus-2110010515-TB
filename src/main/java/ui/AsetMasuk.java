    protected void tabelData(){
        Object[] Baris = {"ID Inventaris Masuk","Kode Barang", "Tanggal Masuk", "Jumlah","Nilai Barang","Kode Lokasi"};
        tabmod = new DefaultTableModel(null, Baris);
        String cariBarang = tfCari2.getText();            
        try{
            String sql = "SELECT * FROM transaksi_inventaris_masuk WHERE kode_inventaris_masuk like '%"+cariBarang+"%' or kode_barang like '%"+cariBarang+"%' order by kode_inventaris_masuk asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                tabmod.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),
                });
            }
            tabell.setModel(tabmod);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal untuk di panggil"+e);
        }
    }

