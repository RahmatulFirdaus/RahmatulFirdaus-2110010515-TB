    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        int ingfo = JOptionPane.showConfirmDialog(null, "Apakah anda yakin menghapus data ini?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        if(ingfo==0){
            String sql = "DELETE FROM transaksi_inventaris_masuk WHERE kode_inventaris_masuk='"+txtidinv.getText()+"'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil untuk di hapus");
                empty();
                tfKodeBarang.requestFocus();
            } catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Data gagal untuk di hapus"+e);
            }
            tabelData();
        }
    }//GEN-LAST:event_bHapusActionPerformed

    private void bEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditActionPerformed
        String sql = "UPDATE transaksi_inventaris_masuk SET kode_barang=?,jumlah=?,nilai_barang=?,tanggal_masuk=?,kode_lokasi=? WHERE kode_inventaris_masuk='"+txtidinv.getText()+"'";
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String fd = sdf.format(jtgl.getValue());
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tfKodeBarang.getText());
            stat.setString(2, tfJumlah.getText());
            stat.setString(3, tfNilaiBarang.getText());
            stat.setString(4, fd);
            stat.setString(5, tfKodeLokasi.getText());
    
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil di ubah");
            empty();
            tfKodeBarang.requestFocus();
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal di ubah"+e);
        }
        tabelData();
    }//GEN-LAST:event_bEditActionPerformed

    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {
        String sql = "INSERT INTO transaksi_inventaris_masuk VALUES (?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtidinv.getText());
            stat.setString(2, tfKodeBarang.getText());
            stat.setString(4, tfJumlah.getText());
            stat.setString(5, tfNilaiBarang.getText());
            java.util.Date date = (java.util.Date) jtgl.getValue();
            stat.setDate(3, new java.sql.Date(date.getTime()));
    
            stat.setString(6, tfKodeLokasi.getText());
            stat.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Data berhasil di simpan");
            empty();
            tfKodeBarang.requestFocus();
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal di simpan " + e);
        }
        tabelData();
    }                                       

    private void tabellMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabellMouseClicked
        int bar = tabell.getSelectedRow();
        String id_inv = tabmod.getValueAt(bar, 0).toString();
        String kdbrg = tabmod.getValueAt(bar, 1).toString();
        String jml = tabmod.getValueAt(bar, 3).toString();
        String nil = tabmod.getValueAt(bar, 4).toString();
        // String tgl = tabmod.getValueAt(bar, 4).toString();
        String kdlok = tabmod.getValueAt(bar, 5).toString();
        try{
            String sql = "SELECT * FROM master_barang WHERE kode_barang ='"+kdbrg+"'";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            if (hasil.next()) {
                tfNamaBarang.setText(hasil.getString("nama_barang"));
                tfKategoriBarang.setText(hasil.getString("kategori_barang"));
                tfMerek.setText(hasil.getString("merek"));
                tfUkuran.setText(hasil.getString("ukuran"));
            }
            String sql2 = "SELECT * FROM master_lokasi WHERE kode_lokasi ='"+kdlok+"'";
            Statement stat2 = conn.createStatement();
            ResultSet hasil2 = stat2.executeQuery(sql2);
            if (hasil2.next()) {
                tfKodeLokasi.setText(hasil2.getString("kode_lokasi"));
                tfNamaLokasi.setText(hasil2.getString("nama_lokasi"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal untuk dipanggil " + e);
        }
                // SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
                // String fd = sdf.format(jtgl.setValue(tgl));
        tfKodeBarang.setText(kdbrg);
        tfJumlah.setText(jml);
        tfNilaiBarang.setText(nil);
                // jtgl.setV(tgl);
        tfKodeLokasi.setText(kdlok);
    }//GEN-LAST:event_tabellMouseClicked

